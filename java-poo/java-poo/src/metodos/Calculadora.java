package metodos;

public class Calculadora {
    
    public double somar(double primeiroValor, double segundoValor) {
        
        return primeiroValor + segundoValor;
    }
    
    public double subtrair(double primeiroValor, double segundoValor) {
        
        return primeiroValor - segundoValor;
    }
}
