package com.app.app_componentes_listagem;

public class Alimento {

    public int id;
    public String nome;
    public double calorias;

    public Alimento() { }

    public Alimento(int id, String nome, double calorias) {
        this.id = id;
        this.nome = nome;
        this.calorias = calorias;
    }

}
