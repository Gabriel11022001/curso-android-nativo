package exercicios.java.exercicio6;

public class Departamento {
    
    private int codigo;
    private String nome;
    
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public String getNome() {
        
        return this.nome;
    }
    
    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
    
    public int getCodigo() {
        
        return this.codigo;
    }
}
